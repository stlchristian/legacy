									<p>Greetings in the strong name of Jesus Christ!  I hope this message finds you happy, doing well in your studies, and anticipating SLCC’s Spring Commencement exercises, to be held at First Christian Church of Florissant!  I am very excited to think that soon you will be walking across the stage to receive your diploma and be recognized for the significant achievement it represents.</p>
									<p>Because we are expecting a near-record number of graduates and their supporters, your cooperation is particularly appreciated as we work together to make the Commencement service memorable, Christ-honoring, and dignified.  There are many details involved in a successful ceremony.  Those having to do with rehearsal, caps and gowns, yearbook photos, and other similar matters are addressed in the accompanying message.  Please pay careful attention to those instructions, and feel free to contact us with any questions.</p>
									<p><strong><u>Commencement Rehearsal:</u></strong> Rehearsal is mandatory for all graduates and marshals.  Rehearsal will be on Friday, May 3th at 7:00 pm at First Christian Church in Florissant.  It will last approximately one hour.</p>
                                    <p><strong><u>Commencement:</u></strong> Commencement is May 4, 2012.  Graduates and Marshals need to be at the church by 8:45 a.m.   Graduation will begin at 10:00 am.</p>
                                    <p><strong><u>Reception:</u></strong> There will be a cake and punch reception immediately following Commencement in the church foyer.</p>
                                    <p><strong><u>Caps &amp; Gowns:</u></strong> Your cap, gown, & tassel will be available for you to pick up in the Bookstore by the end of April at the latest.   If you cannot pick it up before graduation, please call the Bookstore and request that it be delivered to you on the evening of rehearsal.  Everything is yours to keep.</p>
                                    <p><strong><u>Required Graduation Attire:</u></strong> For men, dark slacks, dark socks, dark shoes, dress shirt, and tie.  For women, dark slacks/skirt and blouse or dress and dark shoes.  These items will be visible even when wearing your robe.  Thank you for honoring this formal event and observing the principle of modesty by dressing appropriately. <em>Graduates may not wear fraternity stoles or other regalia or symbols not sponsored by Saint Louis Christian College.</em></p>
                                    <p><strong><u>Graduating with Honors:</u></strong> Those who are graduating with honors (<em>cum laude, magna cum laude, summa cum laude</em>) will receive a gold cord as part of their graduation ensemble. <u>Honor cords must be returned or purchased for $5.00</u></p>
                                    <p><strong><u>Your Diploma:</u></strong> Every graduate will be handed an imitation diploma during the ceremony.  You will receive the actual diploma after the service, unless there is any outstanding course work and/or financial obligations to the library (including books that need to be returned) or the business office.<br />
                                    <p><strong><u>Yearbook Pictures:</u></strong> Graduates still needing to have their picture taken may do so with Alice Perrey between 8:30 and 9:30 a.m. the morning of graduation at the church.</p>
                                    <p><strong><em>Please also note that throwing of the caps is not permitted.</em></strong></p>
