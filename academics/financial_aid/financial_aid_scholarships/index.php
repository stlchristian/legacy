						  <p align="left">SLCC offers around 50 merit scholarships to both returning and new students (approximately 30 to returning students and 20 to new students) each year.  The amounts of these scholarships are generally $1,000 for the year.<br />
For returning students, minimum criteria for consideration will be (1) full-time enrollment, (2) good disciplinary standing, (3) cumulative GPA of 3.0 or higher (for returning students), (4) Fall 2012 MAP grade of 3.0 or higher, and (5) submission of your 2013-2014 FAFSA by April 1, 2013.    Deadline for returning students is April 1, 2013.<br />
Returning students may obtain a scholarship application from the Financial Aid Office.<br /><br />

Around 20 merit scholarships will be awarded to new students.  Contact the Admissions Office for an application and  criteria information.<br /><br />

Students can also apply for one of the school’s traveling teams (Camp Team or Music Ensemble).  Camp Team participants usually are awarded $1,000 per semester after the summer in which they serve, and music ensemble students receive approximately $400 a semester for their participation.<br /><br />

Five students per semester are hired as resident assistants in the dormitories.  These positions pay $1,000 per semester.<br /><br />

The Boatman Scholarship is awarded each year to one or two students who are children of SLCC alumni.
No application is required.
</p>
						  <p align="center"><span class="style1">FEDERAL FINANCIAL AID</span></p>
						  <p align="left">SLCC participates in the following federal financial aid  programs: Federal Pell Grant, Federal Supplemental  Educational Opportunity Grant, Federal Work  Study, and Federal Stafford and PLUS Loans. See the section &ldquo;How to Apply&rdquo; for application instructions. For more information on the federal financial aid programs,  consult the Department of Education&rsquo;s <strong>The  Guide to Federal Student Aid &nbsp;(</strong><u><a href="http://www.studentaid.ed.gov/" target="_blank">www.studentaid.ed.gov/</a></u><strong>)</strong>&nbsp;This guide gives information about general  eligibility requirements for federal aid as well as extensive information about  the available programs.</p>
						  <p align="left"><strong>Federal Pell Grant</strong><br />
						    A Federal Pell Grant does not have to be repaid.&nbsp; The amount of the award is based on your  FAFSA results and enrollment status. The maximum Pell Grant for the 2010-2011 academic year is $5,550 for a student enrolled full-time in two semesters.</p>
						  <p align="left"><strong>Federal Supplemental  Educational Opportunity Grant (FSEOG)</strong><br />
						    The FSEOG does not have to repaid.&nbsp; This grant is awarded to students with the  greatest need as determined by your FAFSA results. &nbsp;&nbsp;At SLCC  the amount of the grant is usually $200 per semester.&nbsp;&nbsp; FSEOG funding at SLCC is limited; funds are  initially awarded to students with an EFC of 0; if additional funds remain at  the end of the year, those students who still have eligibility may receive an  increased amount.</p>
                          <p><strong>Federal Work Study (FWS)</strong><br />
                            SLCC employs a limited number of students for campus jobs. Some of these are funded through the Federal Work Study program. SLCC receives a small amount of funding for federal work study jobs and students under the FWS program have demonstrated financial need. FWS jobs will generally be awarded only to sophomores and above who have completed at least one year of the college's MAP program. Students apply for campus jobs through the various departments on campus, are approved for hiring by the department heads, and those eligible for federal work study funding may be employed under that program.</p>
						  <p><strong>Federal Stafford Loan&nbsp; </strong><br />
						    Stafford Loans are low-interest federal loans available to  students who are attending college at least half time.&nbsp; The amount a student may borrow is based on  year in school, dependency status, and remaining need after deducting other  financial aid from cost of attendance.&nbsp;&nbsp; &nbsp;Maximum amount is $5,500 for  freshmen dependent students ($2,000 of which must be unsubsidized); $6,500 for  sophomores ($2,000 of which must be unsubsidized), and $7,500 for juniors and  seniors ($2,000 of which must be unsubsidized).&nbsp;  Independent students may borrow an additional amount in unsubsidized  loan ($4,000 for freshmen and sophomores; $5,000 for juniors and seniors).&nbsp; Students begin paying back the loan six  months after they have graduated or are no longer enrolled at least half  time.&nbsp;&nbsp; Interest rates are established by  the government and are announced each year in July.&nbsp;Beginning       with the 2010-2011 academic year (July 1, 2010 &ndash; June 30, 2011), all       Stafford Loans at Saint Louis Christian College will be through the Direct       Stafford Loan Program.&nbsp; </p>
						  <p align="left"><strong>Subsidized and  Unsubsidized &ndash; What&rsquo;s the Difference?</strong><br />
						    A subsidized loan is awarded based on need; &ldquo;subsidized&rdquo;  means that the government will pay all interest on the loan until your loan  goes into repayment.&nbsp; Unsubsidized loans  are not based on need but interest accrues on the loan from the day the loan is  disbursed.&nbsp; &nbsp;&nbsp;How is that need determined?&nbsp; Your cost of attendance, less your EFC (from  your FAFSA results) less grants and scholarships you receive will determine eligibility  for a portion of your loan to be subsidized.&nbsp;  Grade level also limits the amount of your loan that can be  subsidized.&nbsp;&nbsp; For unsubsidized portions  of your loan, you may pay the interest while you are in school or you may have  it added to the principal of the loan.&nbsp;&nbsp; </p>
						  <p align="left"><strong>Entrance Loan  Counseling for Stafford Loans</strong><br />
						    Federal law requires that all first-time Stafford borrowers  at SLCC receive entrance loan counseling before the loan is disbursed.&nbsp; All Stafford borrowers must also complete  exit counseling when ready to graduate, leave the college, or drop below  half-time attendance.&nbsp; Loan counseling  can be completed on the following website:&nbsp; <a href="http://www.mappingyourfuture.org" target="_blank">www.mappingyourfuture.org</a>.</p>
						  <p align="left"><strong>PLUS Loans</strong><br />
						    A parent may borrow a Federal PLUS Loan to help pay the  expenses of a child who is a dependent undergraduate student enrolled at least  half time.&nbsp; The parent must pass a credit  check to be eligible to receive a PLUS Loan.&nbsp;  In the event the parent is denied due to adverse credit, the student  generally becomes eligible to borrow an additional amount in an Unsubsidized  Federal Stafford Loan. Beginning with the 2010-2011 academic year (July  1, 2010 &ndash; June 30, 2011), all PLUS&nbsp; Loans  at Saint Louis Christian College will be through the Direct PLUS&nbsp; Loan Program.&nbsp;&nbsp;&nbsp; </p>
						  <p align="center"><strong>&nbsp;</strong><span class="style1">PRIVATE SCHOLARSHIPS</span></p>
						  <p align="left">						    Check with your high school counselor&rsquo;s office to learn of  scholarships for which you may be eligible.&nbsp;  In addition, you may want to go online to do some scholarship  searches.&nbsp; &nbsp;One useful website is <a href="http://www.fastweb.com" target="_blank">www.fastweb.com</a>. &nbsp;&nbsp;FastWeb&rsquo;s free scholarship search includes  more than 600,000 scholarships worth more than $1 billion.&nbsp; Many churches offer assistance to students studying for  ministry in Bible colleges; check with your church leaders to see if they offer  such a scholarship.</p>
						  <p align="center"><span class="style1">VETERANS BENEFITS</span></p>
						  <p align="left">						    Saint Louis Christian College is approved for certification  of students eligible to receive educational assistance from the Department of  Veterans Affairs.&nbsp; &nbsp;Contact your local VA office for information  on eligibility. SLCC       participates in the Yellow Ribbon Program for veterans eligible for       Chapter 33 benefits. 
						  Students with potential VA benefits should contact the SLCC  Registrar&rsquo;s Office (314-837-6777 X 1509) for information on how to proceed.</p>
						  <p align="center"><span class="style1">VOCATIONAL  REHABILITATION</span></p>
						  <p align="left">						    Students with physical disabilities which constitute  substantial vocational handicaps may be eligible for aid covering tuition and  fees.&nbsp;&nbsp; Students with certain visual and  auditory losses may qualify as well.&nbsp;  Students should contact their local Division of Vocational  Rehabilitation office for an evaluation.&nbsp;  If approved, DVR will notify the school .<strong></strong></p>
						  <p align="center"><span class="style1">STATE AID</span></p>
						  <p align="left">						    SLCC does not participate in the Missouri state grant or  scholarship programs, as Missouri does not make these funds available to  students attending theological institutions.&nbsp;  Most other states&rsquo; financial aid programs are available only to students  from that state who attend instate institutions.</p>
						  <p align="center"><span class="style1">ALTERNATIVE EDUCATION  LOANS (non-government loans)</span></p>
						  <p align="left">						    Some students turn to alternative (private) education loans  when the federal loans don&rsquo;t provide enough money to cover their school  costs.&nbsp; Private education loans tend to  cost more than the loans offered by the federal government but are less  expensive than credit card debt.&nbsp;&nbsp;&nbsp; Since  federal education loans are less expensive than credit debt and offer better terms than  private student loans, you should exhaust your eligibility for federal loans  before resorting to private student loans. SLCC       does not recommend any lenders for alternative education loans.  &nbsp; Interested students can find information       on these loans through an Internet browser under the title of &ldquo;alternative       education loans.&rdquo;</p>
						  <p align="center"><span class="style1">CODE OF CONDUCT</span></p>
						  <p align="left">						    The  Higher Education Opportunity ACT (HEOA) requires educational institutions to  develop and comply with a code of conduct that prohibits conflicts of interest  for financial aid personnel.&nbsp; Any SLCC  financial aid office employee who has responsibilities with respect to student  educational loans must comply with this code of conduct.&nbsp; The following provisions bring SLCC into  compliance with the federal law.</p>
                          <p align="left">							Neither SLCC as an institution nor any individual financial aid employee shall enter into any revenue-sharing arrangement with any lender which makes Title IV loans to students attending the institution.</p>
                          <p align="left">							No financial aid employee of SLCC who has responsibilities with respect to education loans, or any of their family members, shall solicit or accept any gift from a lender, guarantor, or servicer of education loans.</p>
                          <p align="left">							For purposes of this prohibition, the term “gift” means any gratuity, favor, discount, entertainment, hospitality, loan or other item having a monetary value of more than a nominal value.</p>
                          <p align="left">							Gifts and favorable terms and benefits does not include:  a brochure, workshop or training using standard materials relating to a loan, default aversion, or financial literacy, such as a part of a training session.  Entrance and exit counseling as long as the institution’s staff are in control of the counseling and the counseling does not promote the services of a specific lender.</p>
                          <p align="left">							A financial aid office employee at SLCC who has responsibilities with respect to education loans shall not accept from any lender or affiliate of any lender any fee, payment, or other financial benefit (including the opportunity to purchase stock) as compensation for any type of consulting arrangement or other contract to provide services to a lender or on behalf of a lender relating to education loans.</p>
                          <p align="left">							SLCC shall not:<br />
                          												<ul>
                                                                        	<li>Assign a lender to a first-time borrower through award packaging or any other method, or</li>
                                                                            <li>Refuse to certify or delay certification of any loan based on the borrower’s selection of a particular lender or guaranty agency.</li>
                                                                        </ul></p>
                          <p align="left">							SLCC shall not accept from any lender any offer of funds to be used for private education loans, including funds for an opportunity pool loan, to students in exchange for the institution providing concessions or promises regarding providing the lender with:
                          												<ul style="list-style-type:none;">
                                                                        	<li style="list-style-type:none;">A specific number of loans made, insured, or guaranteed under Title IV;</li><br />
                                                                            <li style="list-style-type:none;">A specific volume of such loans; or</li><br />
                                                                            <li style="list-style-type:none;">A preferred lender arrangement for such loans.</li>
                                                                        </ul>
                          <p align="left">							An “opportunity pool loan” is defined as a private education loan made by a lender to a student (or the student’s family) that involves a payment by the institution to the lender for extending credit to the student.</p>
                          <p align="left">							SLCC shall not request or accept from any lender any assistance with call center staffing or financial aid office staffing.</p>
                          <p align="left">							Any financial aid office employee who has any responsibilities with respect to education loans or other student financial aid, and who serves on an advisory board, commission, or group established by a lender, guarantor, or group of lenders or guarantors, shall be prohibited from receiving anything of value from the lender, guarantor, or group of lenders or guarantors, except that the employee may be reimbursed for reasonable expenses incurred in serving on such an advisory board, commission, or group.</p>
						  <p align="center"><span class="style1">COLLEGE PAYMENT PLAN</span></p>
						  <p align="left">						    Saint Louis Christian College does not offer a payment  plan.&nbsp; All institutional charges are to  be paid in full before the first day of classes each semester unless the  student has sufficient financial aid to cover them.</p>
