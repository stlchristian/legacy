  <br />
  <span class="style7"><strong>If I have sufficient financial aid to cover my costs, do I need any  cash to start classes?</strong></span><br />
  <br />
  <span class="style3">If you have  sufficient financial aid, you will not need any cash at the time you begin classes. If you have enough financial  aid to cover books, your books can be added to your school account also.</span><br />
