						  <p><span class="style7"><strong>I already filed the  FAFSA but did not list Saint Louis Christian College as one of my schools</strong></span><br />
						    <br />
						    <span class="style3">You can make a correction online and add our school  code.&nbsp; Go to <a href="http://www.fafsa.ed.gov" target="_blank">www.fafsa.ed.gov</a> and select the link ADD OR  DELETE  A SCHOOL CODE.&nbsp; SLCC&rsquo;s code is <strong>012580.</strong></span><br />
  <strong><br />
  <span class="style7">I am a dependent student, but my parent refuses to sign the FAFSA or submit FAFSA information.</span></strong><br />
						    <br />
						    <span class="style3">If there are unusual circumstances, such as abuse, abandonment, etc., the Financial Aid Office may be able to do a dependency override if you provide sufficient documentation. Contact the Financial Aid Office to request a Dependency Override form if this is the case. If circumstances do not warrant a dependency override, there is a provision in which a student whose parents refuse to cooperate in completing the FAFSA may be able to receive an unsubsidized Stafford Loan. Contact the Financial Aid Office for more information.</span></p>
						  <p><span class="style7"><strong>What determines the  Expected Family Contribution (EFC) when I file my FAFSA?</strong></span><br />
						    <br />
						    <span class="style3">Family income is one factor, but not the only one.&nbsp; Family size, number in college, untaxed  income, assets, etc. are also taken into consideration.&nbsp; The student&rsquo;s own income and assets are  assessed more heavily than the parents&rsquo;.</span><br />
  <strong><br />
  <span class="style8">What if my family&rsquo;s  financial situation has changed considerably from the information we reported  on the FAFSA?</span></strong><span class="style7"><br />
  </span><br />
  <span class="style3">The Financial Aid  Office may be able to do an income adjustment based on the current  situation.&nbsp; Request an Income Adjustment  Form from the Financial Aid Office.</span><br />
  <strong><br />
  <span class="style8">My parents are  divorced.&nbsp; My mother is remarried and my  stepfather hasn&rsquo;t adopted me.&nbsp; Is my  stepfather required to supply his financial information on the FAFSA?</span></strong><br />
						    <br />
						    <span class="style3">Yes.</span><br />
  <strong><br />
  <span class="style8">I am an independent  married student and my wife and I filed our tax returns separately.&nbsp; Do I need to include her income on my FAFSA?</span></strong><br />
						    <br />
						    <span class="style3">Yes.&nbsp; When asked for  income and taxes paid and other tax items, you must total the amounts from both  returns.</span><br />
  <strong><br />
  <span class="style8">My parents are  divorced.&nbsp; I live with my mother, but my  father claims me on his tax return.&nbsp;  Whose information do I report on the FAFSA?</span></strong><br />
  <br />
  <span class="style3">Complete the FAFSA  with your information and your mother&rsquo;s information.&nbsp; Only the custodial parent (parent with whom  the child lives) is required to supply the necessary financial  information.&nbsp;&nbsp; If the father furnishes  child support, it will be reported on the FAFSA also.</span><br />
  <span class="style2"><br />
  </span><span class="style2">
  What if my parents&rsquo;  income tax return has not been completed when I file the FAFSA?</span><br />
						    <br />
						    <span class="style3">You may use an estimate to file the FAFSA; however, your  FAFSA will probably be selected for verification, and you will have to provide  complete federal income tax information before actual awards will be made.</span><br />
  <strong><br />
  <span class="style8">What does it mean if  I am selected for a process called Verification?</span></strong><br />
						    <span class="style3"><br />SLCC&rsquo;s Financial Aid Office is required to obtain certain  documents from students selected for verification. You will be notified by the SLCC Financial Aid Office regarding what documents are required.</span><br />
                            <span class="style8"><strong><br />Can I obtain a job on  campus?</strong></span><br />
						    <span class="style3"><br />Funding for jobs on campus is limited; jobs are assigned  primarily to returning students who have demonstrated&nbsp; competence and dependability in their  Ministry Advancement Program assignments the previous year.&nbsp; Freshmen generally will not be assigned paid  jobs on campus.</span><br />
  <strong><br />
  <span class="style8">If my SLCC tuition  scholarship, Pell Grant, and Stafford loan does not provide enough financing to  meet my educational needs, what else is available?</span></strong><br />
						    <span class="style3"><br />You may want to consider a <a href="../financial_aid_scholarships/Request_Plus_loan.pdf" target="_blank">Parent PLUS Loan</a> (parents of  undergraduate students only).&nbsp; The parent  must apply and be approved; if the parent is not approved due to a faulty  credit rating, the student may be able to receive an additional $4,000 a year  in unsubsidized Stafford Loan.&nbsp; Proof of  denial must be&nbsp;&nbsp; received by the  Financial Aid Office.&nbsp;&nbsp; If your parent  does not wish to apply for a PLUS loan, you might want consider an Alternative  (private) educational loan.&nbsp;&nbsp; More  information about PLUS and alternative loans are on the <strong><a href="../financial_aid_scholarships/Loan_Request_Form.pdf" target="_blank">SLCC Loan Request Form</a></strong>.&nbsp;  An alternative loan should be a loan of last resort, since terms and  conditions are not as favorable as for the Federal Stafford and PLUS loans.</span><br />
                            <span class="style2"><br />I am a transfer  student.&nbsp; What determines my grade level  for Stafford Loan purposes?</span><br />
						    <br />
						    <span class="style3">Once your admissions file is complete, the Registrar will  evaluate your previous transcripts depending on what your intended degree  program will be and determine how many of your credits are accepted for transfer  to that program.&nbsp; The number of credits  accepted will determine your grade level for loan purposes.</span><br />
  <strong><br />
  <span class="style8">After applying for  financial aid, what do I do next?</span></strong><br />
						    <br />
						    <span class="style3">Once you have completed all the proper paper work needed for  financial aid, you will receive a Financial Aid Award Letter in the mail.&nbsp; This letter will indicate the aid that has  been awarded.&nbsp; Review the award letter  for accuracy and to make sure your financial aid is sufficient to cover your  costs.&nbsp;&nbsp; Read all of the accompanying  material to make sure you understand the terms and conditions of your financial  aid.&nbsp; &nbsp;You do not need to sign and return the award  letter unless you reject one or more of the awards.</span></p>
