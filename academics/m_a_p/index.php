                    	<center><h3>MAP - The Ministry Advancement Program at<br />St. Louis Christian College</h3></center>
                        <p>Welcome to MAP at SLCC! The Ministry Advancement Program (MAP) is part of the Professional Field Experience (PFE) graduation requirement at Saint Louis Christian College. Students receive a grade for completed assignments and 1/2 credit per semester. Three MAP credits are required for graduation for bachelor’s degree and two credits of MAP are required for an associate’s degree.</p>
                        <p>Saint Louis Christian College believes in ministry and exists for the purpose of training men and women to become servant-leaders in God's Kingdom. The MAP program is designed to provide real and meaningful ministry experience.</p>
                        <p>MAP assignments are based on the need for ministry experience and résumé building before college graduation. The MAP professional field experience commitment can average several hours per week as students learn the value of a work ethic through their work on campus; explore their ministry callings by attending chapel, small groups and professional conferences in the fall and spring semesters; and develop their own leadership skills and gifts on the campus and in churches. Because MAP requires this time commitment, it is important to keep this in mind when planning course schedules and responsibilities.</p>
                        <p>MAP is divided into three phases with different syllabus requirements for each phase. Each phase consists of two passing semesters.</p>
                        <p>The goal for each student graduating from Saint Louis Christian College is involvement as a servant-leader in ministry. The Ministry Advancement Program ensures that graduates of Saint Louis Christian College have participated and advanced in ministry, so they will be competent servant-leaders in urban, suburban, rural, and global ministries.</p>
                        <center><h3>MAP Overview</h3></center>
                        <h3>Program Description</h3>
                        The Ministry Advancement Program (MAP) connects classroom instruction with practical experience. It is designed to provide opportunities for growth and leadership in the three areas of: Christian Character, Christian Community, and Christian Calling. Students receive 1/2 credit per semester, and a grade is given for the course each semester. This is a transferrable credit in the category of Professional Field Experience.
                        <h3>Program Objectives</h3>
                        <ol>
                        	<li>Student growth in Christian Community through leadership within church fellowships and parachurch organizations.</li>
                            <li>Student growth in Christian Calling through spiritual and professional development opportunities.</li>
                            <li>Student growth in Christian Character through service.</li>
                        </ol>
                        <h3>Phase One Program Rationale</h3>
                        <p>Phase One of MAP, designed for freshman and most transfer students, emphasizes Christian character through service and small accountability groups.</p>
                        <h3>Phase Two Program Rationale</h3>
                        <p>Phase Two of MAP, designed for sophomores and second year transfer students, emphasizes Christian Community through service and ministry on the Saint Louis Christian College campus, within church and small group fellowships, and the culture of society. Students enter this phase after successful completion of two semesters in Phase One. Two successful semester are required to complete this phase.</p>
                        <h3>Phase Three Program Rationale</h3>
                        <p>Phase Three of MAP, designed for juniors, seniors, and third year transfer students, emphasizes Christian Calling through the practice and study of leadership skills on the Saint Louis Christian College campus within churches and small group fellowships, and in the culture of the society.  Two successful semesters are required to complete this phase.</p>
                        <h3>MAP Grades</h3>
                        <ol>
                        	<li>MAP grades are calculated into the student's grade point average (GPA). Student athletes must take this into consideration when calculating grades for eligibility.</li>
                            <li>Students must pass two semesters in a phase before going to the next phase.</li>
                            <li>The possible consequences of failing MAP: 1) Lose school scholarships. 2) Lose eligibility for academic honors. 3) Lose eligibility for faculty nomination into honors or scholarship societies.</li>
                        </ol>
