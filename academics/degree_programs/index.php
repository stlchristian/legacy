						<font class="section_heading">TESOL Certificate</font>
                        <p>Saint Louis Christian College offers a “Teaching English to Students of other Languages” (TESOL) certificate, a 9-hour program consisting of 3 courses meeting the core standards established by the international TESOL Organization.</p>
                        <p>The program prepares students to teach English as a second language overseas where a TESOL certificate is required, and it equips students to teach ESL (English as a Second Language) as an outreach ministry through churches and Christian organizations in the U.S.  In addition, the program provides a solid preparation for students who desire to pursue a Master of Arts degree in one of many ESL/TESOL university programs.</p>
                        <p>The TESOL program appeals particularly to three groups of individuals:</p>
                        <ol>
                            <li>Students enrolled in Saint Louis Christian College’s Intercultural and Urban Missions (IUM) specialization. The TESOL certificate is part of IUM program.<br /><br /></li>
                            <li>Students enrolled in Saint Louis Christian College’s other specializations. For the BS degree, two of the TESOL courses may be taken as general education electives; the third course would be an additional 3 hours beyond those required for the degree. For the BA degree, the three TESOL courses would be an additional 9 hours beyond those required for the degree.<br /><br /></li>
                            <li>Individuals not already enrolled in courses at Saint Louis Christian College. The three courses are taken for non-credit; a designated TESOL certificate course price applies. Individuals are exempt from the application process, and they must be at least 18 years old. Those interested should contact the Academic Office for further information.</li>
                        </ol>
                        <p>The TESOL certificate is unrelated to the Bible certificate program. The specific TESOL courses are listed in the Course Descriptions in the College catalog.</p>
