                            		<h2>Consumer Information</h2>
                                	<h3>Revised Summer 2013</h3>
                                </center>
                                <p><em>Saint Louis Christian College is pleased to furnish this information in compliance with the Student Right-to-Know Act of 1993 and the Higher Education Authorization Act of 2009.  Interested parties should consult the online college Catalog and college personnel in the SLCC administrative offices for further information, as indicated below.  They also may access the College Navigator website <a href="http://nces.ed.gov/collegenavigator/">http://nces.ed.gov/collegenavigator/</a> maintained by the National Center for Education Statistics for comparative information for all U.S. colleges and universities.  Enrollment statistics provided there are compiled from IPEDS (Integrated Postsecondary Education Data System) reports filed annually by each college with the federal government.</em></p>
                                
                                <h3>Student Diversity</h3>
                                <p>According to enrollment data for spring term 2013, 58% of full time students were male, 42% female.  Ethnicity was self-reported as follows:  57% White (non-Hispanic), 33% Black (non-Hispanic), 4% Hispanic; one each Asian and Native American; 2% Non-resident Alien; 3% Mixed Race; and 1 Unknown.   PELL Grants were awarded to 53% of students during the 2012-2013 academic year.</p>
                                
                                <h3>Retention (Student Persistence)</h3>
                                <p>The most recent IPEDS data show a retention rate of 48% for FTFT (First Time Full Time) Freshmen entering in the Fall of 2011.  Overall retention of all full time students from Fall 2011 to Fall 2012 was 66%.
                                
                                <h3>Completion</h3>
                                <p>Data for full time Freshmen students from 2003-2006 show a Completion Rate of 39%.  The reporting standard for Completion specifies a time frame of 150 percent of published time to complete a Bachelor’s degree.  (Thus the reporting format allows six years to complete a four-year program.)</p>
                                <p><em>Note:  Many colleges use a reporting formula for Completion that excludes entering students who do not intend to finish a full four-year program at that college.  Saint Louis Christian College includes in its Completion rate every First Time Full Time student, including those who perceive a benefit from enrolling for fewer credits than the number required to complete a degree.  Colleges also may report students as completing a degree program who transfer to another college before doing so.  Saint Louis Christian College reports students who transfer to another college as non-completers.</em></p>
                                
                                <h3>Placement</h3>
                                <p>Bachelor’s degree recipients in the graduating classes of 2010-2013 indicated their plans after graduation as follows:</p>
                                <p>35% Enter or remain in paid ministry positions (Youth, Preaching, Worship, Chaplaincy, etc.).<br />
                                28%  Pursue a further degree beyond SLCC (Seminary, University, Vocational).<br />
								23% Will serve in unpaid ministry or secure other employment, or both.<br />
								9% Enter overseas or domestic cross-cultural ministry (Church Planting, Benevolent, etc.).<br />
								5% Serve with their spouse in a volunteer ministry capacity.</p>
                                
