				<h2>Campus Security </h2>
                    <p><em> At St. Louis Christian College, the safety and well-being of our students, guests, faculty, and staff are always a priority. However, a truly safe campus can only be achieved through the cooperation of everyone. This information is a part of our effort to ensure that this collaborative endeavor is effective. We hope that you will read it carefully and use the information to help foster a safe environment for yourself and the campus. </em></p>
                    <h3> Mission Statement </h3>
                    <p> It is the mission of the office of Campus Security to complement and support the advancement of the educational purposes of the College by providing a safe and secure environment for learning, working, and personal development.</p>
                    <p> When Campus security is notified of criminal activity, request for assistance, or any type of emergency situation, members of the security team respond as quickly as possible. Requests for information and assistance are promptly fulfilled.</p>
                    <h3> Philosophy</h3>
                    <p> It is the philosophy of the campus safety service department that we would much rather prevent crimes from occurring than react to them after the fact. A primary vehicle for accomplishing this goal is safety awareness and crime prevention. Information about the Campus Security and personal safety and security issues are distributed at various locations throughout the campus. </p>
                    
                    <h3>Emergency Response</h3>
                    <p>SLCC has developed a comprehensive emergency response plan. <strong><a href="emergency-response-plan.pdf" target="_blank">Please click here</a></strong> to view it.</p>
                    
                    <h3>Annual Reporting</h3>
                    <p>SLCC has made available for distribution an Annual Security Report and an Annual Campus Fire Safety Report, combined in one document. <strong><a href="annual_crime_report.pdf" target="_blank">Please click here</a></strong> to view the annual reports for 2012.</p>
                    
