                    <h3>Safety Patrols</h3>
                    <p>All campus security officers are licensed by the St. Louis County, Missouri, Police Department. They are also certified in CPR and AED.</p>
                    <p>Parking lots, buildings, and campus areas are actively patrolled by Campus Security Officers. Emphasis is placed on the evening hours, when the need for higher visibility is necessary. </p>
                    <h3> New Student Orientation </h3>
                    <p> A Campus Security presentation accompanied by related printed material is made available to new students during orientation. Crime prevention material is also made available to all community members throughout the year. </p>
                    <h3> Reporting Criminal Activity / Emergencies</h3>
                    <p> When a life-threatening situation arises, dial 9-911 from any campus telephone for outside police or emergency medical assistance. Notify&nbsp;Campus Security at (314) 837-6777 ext. 1206 as soon as possible so that immediate action can be taken to remedy the situation. All crimes on campus will be referred to civil authorities. </p>
                    <p> Members of the SLCC community are urged to always report criminal activities or other emergencies on campus. The Campus Security Department wants to assist in helping you to protect yourself and your property. Campus Security can be reached at (314) 837-6777 ext. 1206 (dial extension 1206 from any campus phone). </p>
                    <p>The College employs security personnel and the campus is well-lit at night. The city of Florissant has an outstanding reputation for a low crime rate.  Current information on criminal incidents (if any) on the campus of Saint Louis Christian College is available on the U.S. Department of Education website at <a href="http://ope.ed.gov/security" target="_blank">http://ope.ed.gov/security</a>.</p>
