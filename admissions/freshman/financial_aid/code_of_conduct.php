						  <p><strong>CODE  OF CONDUCT </strong></p>
						  <p>The Higher Education Opportunity ACT (HEOA) requires  educational institutions to develop and comply with a code of conduct that  prohibits conflicts of interest for financial aid personnel.&nbsp; Any SLCC financial aid office employee who  has responsibilities with respect to student educational loans must comply with  this code of conduct.&nbsp; The following  provisions bring SLCC into compliance with the federal law.</p>
						  <p>Neither SLCC as an institution nor any individual  financial aid employee shall enter into any revenue-sharing arrangement with  any lender which makes Title IV loans to students attending the institution.</p>
						  <p>No financial aid employee of SLCC who has  responsibilities with respect to education loans, or any of their family  members, shall solicit or accept any gift from a lender, guarantor, or servicer  of education loans.</p>
						  <p>For purposes of this prohibition, the term &ldquo;gift&rdquo;  means any gratuity, favor, discount, entertainment, hospitality, loan or other  item having a monetary value of more than a nominal value.</p>
						  <p>Gifts and favorable terms and benefits does not  include:&nbsp; a brochure, workshop or  training using standard materials relating to a loan, default aversion, or  financial literacy, such as a part of a training session.&nbsp; Entrance and exit counseling as long as the  institution&rsquo;s staff are in control of the counseling and the counseling does  not promote the services of a specific lender.</p>
						  <p>A financial aid office employee at SLCC who has  responsibilities with respect to education loans shall not accept from any  lender or affiliate of any lender any fee, payment, or other financial benefit  (including the opportunity to purchase stock) as compensation for any type of  consulting arrangement or other contract to provide services to a lender or on  behalf of a lender relating to education loans.</p>
						  <p>SLCC shall not:</p>
						  <ol>
						    <ol>
						      <ol>
						        <li>Assign a lender  to a first-time borrower through award packaging or any other method, or</li>
						        <li>Refuse to certify  or delay certification of any loan based on the borrower&rsquo;s selection of a  particular lender or guaranty agency.</li>
					          </ol>
					        </ol>
					    </ol>
						  <p>SLCC shall not accept from any lender any offer of  funds to be used for private education loans, including funds for an  opportunity pool loan, to students in exchange for the institution providing  concessions or promises regarding providing the lender with:</p>
						  <p>&nbsp;&nbsp;&nbsp;&nbsp; A specific  number of loans made, insured, or<br />
  &nbsp;&nbsp;&nbsp;&nbsp; Guaranteed  under Title IV;</p>
						  <p>&nbsp;&nbsp;&nbsp;&nbsp; A specific  volume of such loans; or</p>
						  <p>&nbsp;&nbsp;&nbsp;&nbsp; A preferred  lender arrangement for such loans.</p>
						  <p>An &ldquo;opportunity pool loan&rdquo; is defined as a private  education loan made by a lender to a student (or the student&rsquo;s family) that  involves a payment by the institution to the lender for extending credit to the  student.</p>
						  <p>SLCC shall not request or accept from any lender any  assistance with call center staffing or financial aid office staffing.</p>
						  Any financial aid  office employee who has any responsibilities with respect to education loans or  other student financial aid, and who serves on an advisory board, commission,  or group established by a lender, guarantor, or group of lenders or guarantors,  shall be prohibited from receiving anything of value from the lender,  guarantor, or group of lenders or guarantors, except that the employee may be  reimbursed for reasonable expenses incurred in serving on such an advisory  board, commission, or group.<!-- InstanceEndEditable -->
