						  <p align="center"><span class="style1">SATISFACTORY ACADEMIC PROGRESS</span></p>
						  <p align="center" class="style2"> Students must be making satisfactory academic progress to  receive federal financial aid and the SLCC tuition scholarship. A  student whose cumulative grade point average or completion rate fails to meet  the minimum standards will be placed on one semester of financial aid  probation.&nbsp; Failure to bring the  cumulative grade point average up to the required minimum after the  probationary semester will result in financial aid suspension.&nbsp; <br />
                          </p>
						  <p align="center" class="style2">The standards of satisfactory academic  progress are as follows:</p>
						  <p><span class="style3">Federal Financial Aid</span></p>
						  <p>The Department of Education requires a student who is  receiving federal financial aid to be making satisfactory academic  progress.&nbsp; Satisfactory academic progress  is measured both by a student&rsquo;s cumulative grade point average (CGPA) and by  the successful completion of a certain percentage of coursework attempted.&nbsp;&nbsp; Successful completion means credits earned  by receiving one of the following grades:&nbsp;  A,B,C,D.&nbsp; Satisfactory progress  will be measured at the end of each semester.<br />
						    The CGPA requirement takes into account only courses  attempted at SLCC (not transfer hours).&nbsp; </p>
						  <p>After first semester&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1.75 CGPA<br />
						    After second semester&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1.85 CGPA<br />
						    After third semester&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1.95 CGPA<br />
						    After fourth semester&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2.0 CGPA</p>
						  <p>A student whose CGPA fails to  meet the minimum standards noted above will be placed on one semester of warning status.&nbsp; During this warning semester financial aid can be received.&nbsp; Failure to bring the CGPA up to the required  minimum by the end of the warning semester will result in financial aid  suspension.&nbsp; Students on financial aid  suspension cannot continue to receive federal financial aid until the requisite  CGPA is attained.<br />
						    <br>The student is also expected to  make <strong>quantitative</strong> academic progress  toward the completion of his/her degree.&nbsp;  The quantitative measure of academic progress consists of two  parts.&nbsp; The total hours attempted may be  no greater than 150% of the total hours required for the degree program in  which the student is enrolled.&nbsp; Transfer  credits accepted toward the student&rsquo;s degree program will be counted toward  this maximum.&nbsp; A change of major does not  extend the number of credit hours that may be attempted.<br />
						    In addition, the student must  earn at least successfully complete at least 67% of the hours attempted (this  is a cumulative measurement).&nbsp; A student  who fails to meet this requirement will be placed on one semester of financial  aid warning.&nbsp; Failure to meet the 67%  requirement by the end of the probationary semester will result in financial  aid suspension.<br />
						    <br>Withdrawals from a class before  the class has begun or during the drop/add period of the day program will not  affect the completion rate.&nbsp; All other  withdrawals and grades of &ldquo;F&rdquo; will affect the completion rate.<br />
						    Although academic bankruptcy may  be granted to improve a student&rsquo;s CGPA, it will not discount courses attempted  in determining the&nbsp; completion rate for  financial aid purposes.</p>
						  <p><span class="style3">Appeals</span></p>
						  <p>A student who has been placed on financial  aid suspension who feels he or she has circumstances which warrant an exception  to this policy should submit a letter of appeal to the Financial Aid Office  addressed to the Admissions Committee.&nbsp;  Reasons for acceptable appeal are:&nbsp;  Serious illness or accident of the student; death or serious illness in  the immediate family; other extenuating circumstances may be acceptable and  will be considered.<br />
						    <br>If the appeal is accepted, the  student will be notified in writing of the decision.&nbsp; If the appeal is granted, the student will be  on probation for one semester and must complete the requirements set by the  committee to continue receiving aid past the probationary semester.<br />
						    <br>Note:&nbsp; Before withdrawing from classes after the  drop/add period, students should contact the financial aid office to determine  the effect on financial aid.</p>
						
