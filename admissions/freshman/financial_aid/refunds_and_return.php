                          <span class="style2">RETURN OF TITLE IV  FUNDS POLICY</span><br />
						    <br />
						    <span class="style3">The law specifies how your school must determine the amount  of Title IV program assistance that you can earn if you withdraw from  school.&nbsp; &nbsp;The Title IV programs covered by this law  include Federal Pell Grants, Academic Competitive Grants, SEOG Grants, Federal  Stafford Loans, and Federal&nbsp; PLUS  Loans.&nbsp;&nbsp; <br />
						    <br />
						    When you withdraw during your payment period, the amount of  Title IV program assistance that you have earned up to that point is determined  by a specific formula.&nbsp; If you received (or  your school or parent received on your behalf) less assistance than the amount  you earned, you may be able to receive those additional funds.&nbsp; If you received more assistance than you  earned, the excess funds must be returned by the school and/or you.<br />
						    <br />
						    The amount of assistance that you have earned is determined  on a prorata basis.&nbsp; For example, if you  completed 30% of your payment period of enrollment, you earn 30% of the  assistance you were originally scheduled to receive.&nbsp; Once you have completed more than 60% percent  of the payment period, you earn all the assistance that you were scheduled to  receive for that period.<br />
						    If you did not receive all of the funds that you earned, you  may be due a post-withdrawal disbursement.&nbsp;  If your post-withdrawal disbursement includes loan funds, your school  must get your permission before it can disburse them.&nbsp; You may choose to decline some or all of the  loan funds so that you don&rsquo;t incur additional debt.&nbsp; Your school may automatically use all or a  portion of your post-withdrawal disbursement of grant funds for tuition, fees,  and room and board charges (as contracted with the school).&nbsp;&nbsp;&nbsp; The school needs your permission to use the  post-withdrawal grant disbursement for all other school charges.&nbsp; If you do not give your permission (some  schools ask for this when you enroll), you will be offered the funds.&nbsp; However, it may be in your best interest to  allow the school to keep the funds to reduce your debt at the school.<br />
						    <br />
						    There are some federal financial aid funds that you were  scheduled to receive that cannot be disbursed to you once you withdraw because  of other eligibility requirements.&nbsp; For  example, if you are a first-time, first-year undergraduate student and you have  not completed the first 30 days of your program before you withdraw, you will  not receive any FFEL loan funds that you would have received had you remained  enrolled past the 30th day.<br />
						    <br />
						    If you receive (or your school or parent &nbsp;receives on your behalf) excess Title IV  program funds that must be returned, your school must return a portion of the  excess equal to the lesser of:
						    </p>
						    </span>
						    <ol start="1" type="1" class="style3">
                            <li>&nbsp;your institutional charges multiplied by       the unearned percentage of your funds, or</li>
						    <li>the entire amount of       excess funds.</li>
						    </ol>
						  <p><span class="style3">The school must return this amount even if it didn&rsquo;t keep  this amount of your Title IV program funds.<br />
						      <br />
						      If your school is not required to return all of the excess  funds, you must return the remaining amount.&nbsp;  Any loan funds that you must return, you (or your parent for a PLUS  Loan) must repay in accordance with the terms of the promissory note.&nbsp; That is, you make scheduled payments to the  holder of the loan over a period of time.<br />
						      <br />
						      Any amount of unearned grant funds that you must return is  called an overpayment.&nbsp; The maximum  amount of a grant overpayment that you must repay is half of the grant funds  you received or were scheduled to receive.&nbsp;  You must make arrangements with your school or the Department of  Education to return the unearned grant funds.<br />
						      <br />
						      The requirements for Title IV program funds when you  withdraw are separate from any refund policy that your school may have.&nbsp; Therefore, you may still owe funds to the  school to cover unpaid institutional charges.&nbsp;  Your school may also charge you for any Title IV program funds that the  school was required to return.&nbsp; <br />
						      <br />
						      Withdrawal date for Title IV purposes is defined as the student's last day of attendance in a class.<br />
						      <br />
						      The Return of Title IV Funds policy does not apply to AIM  students who have completed at least one course in a semester.&nbsp; &nbsp;For  AIM students who totally withdraw from a semester before completing at least  one course, this policy applies.&nbsp; The  period of enrollment is considered to be the entire semester.&nbsp;&nbsp; If an AIM student has a Pell Grant, the  modules not yet begun will be discounted, and the Pell Grant will be reduced to  the lower enrollment status before calculating the aid to be returned.<br />
