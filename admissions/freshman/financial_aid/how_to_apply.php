						  <p><strong><span class="style1">HOW TO APPLY</span></strong></p>
						  <ul>
						    <li><strong>SUBMIT  THE FAFSA</strong></li>
					    </ul>
						  <p>For any of the Federal Financial Aid programs (including the  Stafford and PLUS Loans), submit the Free Application for Federal Student Aid  (FAFSA) for the appropriate year to the Federal Processor.&nbsp; &nbsp;&nbsp;&nbsp;For  2013-2014, you should have your 2012 federal income tax return(s) completed before  you submit the FAFSA.<br />
						    <br>To  submit the FAFSA online (the preferred method), first go to <a href="http://www.pin.ed.gov" target="_blank">www.pin.ed.gov</a> and obtain a PIN number if you do not already have one.&nbsp; If you are a dependent student, your parent  must also obtain a PIN number.&nbsp; Provide  an email address and the PIN will be sent to your email.&nbsp; You will use the PIN to sign your FAFSA.&nbsp;&nbsp; Then complete the FAFSA online at <a href="http://www.fafsa.ed.gov" target="_blank">www.fafsa.ed.gov</a>. &nbsp;&nbsp;&nbsp;<strong>SLCC&rsquo;s school code is 012580.</strong></p>
						  <p>A  paper FAFSA can be obtained by calling 1-800-433-3243 or by visiting <a href="http://www.edpubs.org" target="_blank">www.edpubs.org</a>.&nbsp; Or you can access a PDF version of the FAFSA  at <a href="http://www.FederalStudentAid.ed.gov">www.FederalStudentAid.ed.gov</a>.&nbsp;  To mail the PDF version of the FAFSA, print it out, complete it, and  mail to the address provided.&nbsp; If you  cannot submit it 30 days before you expect to enroll, you can complete the  paper FAFSA and send it to the SLCC Financial Aid Office for electronic  processing; results can be obtained the next day.&nbsp; </p>
						  <p>About 30%  of all FAFSAs are selected by the Federal Processor for verification. &nbsp;If you are selected, you will be required to  furnish a verification worksheet and and possibly other documents.&nbsp; The Financial Aid Office will notify you if  you are selected and will inform you of the documents you must furnish to the  college.</p>
						  <ul>
						    <li><strong>SUBMIT AN SLCC LOAN REQUEST FORM</strong> &nbsp;</li>
					    </ul>
					    <p>If you  will need either a Stafford Loan or a PLUS (parent&rsquo;s) loan, submit an SLCC <strong>Loan Request Form</strong> to the Financial Aid  Office.&nbsp;</p>
					    <ul>
					      <li><strong>COMPLETE       A MASTER PROMISSORY NOTE (MPN)</strong></li>
				        </ul>
					    <p>The MPN can be done online at <a href="http://www.StudentLoans.gov">www.StudentLoans.gov</a>.&nbsp; Select the green <strong>Sign In</strong> link.&nbsp;&nbsp; You will need your Federal PIN Number (same  as for your FAFSA).&nbsp;&nbsp; After establishing  your profile, select Sign Master Promissory Note and follow the instructions.</p>
					    <ul>
                              <li><strong>COMPLETE       LOAN ENTRANCE COUNSELING</strong></li>
                        </ul>
                            <p>The MPN can be done online at <a href="http://www.StudentLoans.gov">www.StudentLoans.gov</a>.&nbsp; Select the green <strong>Sign In</strong> link.&nbsp;&nbsp; You will need your Federal PIN Number (same  as for your FAFSA).&nbsp;&nbsp; After establishing  your profile, select <strong>Complete Entrance Counseling.</strong>&nbsp; <br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br />
                            For a parent who wants to apply for a PLUS Loan, application  can be done online at <a href="http://www.StudentLoans.gov">www.StudentLoans.gov</a>.&nbsp; Select the green <strong>Sign In</strong> link.&nbsp;&nbsp; Then select the link <strong>Parent Plus.&nbsp; </strong>&nbsp;You will need a PIN Number.&nbsp; (If you obtained a PIN when you signed the  student&rsquo;s FAFSA, that is your PIN Number.) </p>
