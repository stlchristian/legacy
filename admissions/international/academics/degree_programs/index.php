						<font class="section_heading">TESOL Certificate</font>
						<br />
						Saint Louis Christian College offers a "Teaching English to Students of other Languages" (TESOL) certificate, an 18-hour program consisting of 6 courses meeting the core standards established by the international TESOL Organization. (Please note that the TESOL Certificate is unrelated to the AIM program.)
						<br />
						<br />
						The program prepares students to teach English as a second language overseas where a TESOL certificate is required, and it equips students to teach ESL (English as a Second Language) as an outreach ministry through churches and Christian organizations in the U.S. In addition, the program provides a solid preparation for students who desire to pursue a Master of Arts degree in one of many ESL/TESOL university programs.
						<br />
						<br />
						Although the TESOL program targets students pursuing the Intercultural and Urban Missions (IUM) specialization(Second Major), enrollment in the program is open to students of SLCC's other specializations. Non-IUM students, however, are subject to the condition explained in the next paragraph.
						<br />
						<br />
						Once students meet the prerequisites for the program - satisfactory completion of English Composition I and English Composition II - they may begin TESOL courses. Students pursuing the BS in Intercultural and Urban Missions specialization may incorporate the certificate into their degree program without taking additional hours. Students in all other specializations must take an additional 12-18 hours beyond their degree requirements. Descriptions of TESOL courses appear in the Course Description section of the catalog.
