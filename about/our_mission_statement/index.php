		<h3> What does this mission mean? </h3>
		<p><strong>ST. LOUIS...</strong> Located in St. Louis County in Florissant, Missouri, Saint Louis  Christian College has the benefit of being within minutes of vibrant  churches, work opportunities, multiple education institutions, and  cultural experiences.</p>
		<p><strong>CHRISTIAN COLLEGE...</strong> Founded and primarily sustained by members of Christian Churches with a  Stone-Campell Restoration Movement heritage, this institution of higher  education integrates biblical, general, and professional studies with  practical experience.</p>
		<p><strong>PURSUES...</strong> College  requires a serious commitment. We employ every possible means to  accomplish our mission. We move forward in action to see our students  succeed.</p>
		<p><strong>EXCELLENCE...</strong> Superiority in achievement is at the heart of everything we do on our campus.</p>
		<p><strong>IN THE WORD...</strong> The Word of God defines who we are and what we do.</p>
		<p><strong>AND DEVELOPS...</strong> Education is a pilgrimage and a journey.  The development process that takes places in our students takes discipline and time.</p>
		<p><strong>SERVANT LEADERS...</strong> A servant leader has an understanding of ministry developed from the  example and teaching of Christ. It is a hands-on approach to ministry  that is willing to get dirty and serve in the darkest places to make a  Kingdom difference around the world.</p>
		<p><strong>FOR URBAN, SUBURBAN, RURAL, AND GLOBAL MINISTRY...</strong> We prepare our alumni for ministry wherever God calls them.</p>
		<h3> How does this mission impact SLCC? </h3>
		<p>From  its inception, Saint Louis Christian College has provided  Bible-centered higher education to equip men and women as leaders who  can handle the Word and provide ministry with excellence in urban  centers and rural communities around the world. The college was founded  to meet the ministry needs of the Saint Louis region.&nbsp; In June 1956,  three Saint Louis ministers met to discuss a&nbsp;&nbsp; common concern: the need  for leadership in the churches of the Saint Louis region.&nbsp; Luther  Perrine, Hubert Burris, and Vernon Newland reviewed the evangelistic  decline of the previous quarter century and proposed to found a Bible  college as a &ldquo;center of evangelism&rdquo; for the Saint Louis region.&nbsp; Out of  that proposal, Saint Louis Christian College was established.</p>
		<p>In  the first year, faculty responsibilities were distributed among 11  ministers.&nbsp; Nineteen students enrolled.&nbsp; In the following years,  full-time teachers were appointed and an 11.5 acre campus was secured  at the present location. Luther Perrine became the first president in  1957.&nbsp; Russell Boatman became academic dean in 1962, serving in that  capacity through June 1978.&nbsp; Glenn Bourne became president in June  1966.&nbsp; He served in that capacity through June 1978.&nbsp; Saint Louis  Christian College attained accreditation with the American Association  of Bible Colleges and enjoyed growth in facilities, faculty, enrollment  and influence during the Bourne-Boatman administration.</p>
		<p>Thomas  McGee succeeded Glenn Bourne as president in August 1979.&nbsp; He served as  president of the college for 16 years, the longest of any president  during the 39 years of the college&rsquo;s history.&nbsp; During his visionary  leadership, over 300 students were graduated.&nbsp; They served Christ from  coast to coast in the United States and around the world.&nbsp; Under his  leadership, the Adults in Ministry (AIM) degree-completion program was  started, Boldly Beyond 2000 (a capital gifts program for President&rsquo;s  Hall) reached $1.2 million, debt was significantly reduced, and  relationships with churches were strengthened.&nbsp; President McGee  assembled a highly qualified and motivated faculty and staff.&nbsp; Saint  Louis Christian College received an enrollment growth award from <u>Christianity  Today</u> and the AABC.&nbsp; The college had the highest enrollment growth of member  colleges of its size.&nbsp; Enrollment was at 194, the second highest the  school had ever experienced. Saint Louis Christian College continued to  refine and revise programs to achieve the mission of equipping men and  women to impact the world for Christ.</p>
		<p>The 1995-1996  academic year marked the beginning of Dr. Kenneth Beck&rsquo;s tenure as the  fourth president of Saint Louis Christian College until 2004.&nbsp; Dr.  Beck&rsquo;s tenure with the college was highlighted by significant  enhancement of the institution&rsquo;s facilities providing students with a  comfortable environment in which to learn.</p>
		<p>From 2004 -  2006 St. Louis Christian College was led by a five-man Presidential  Transition Team.&nbsp; Team members included two senior ministers; Ben  Merold of Harvester Christian Church and Paul Newland of Gateway  Christian Church; and three operational staff members of SLCC; Jamie  Lankford, finance officer, Kendall Brune, development officer, and  Keith McCaslin, academic officer.&nbsp; The Presidential Team under the  direction of the Board of Trustees led the college in a new strategic  direction. Two years ago Saint Louis Christian College recognized it  was facing the growing challenge of recruiting a relatively small pool  of students, given the very specific nature of the type of degrees the  college offers, in a highly competitive market.&nbsp; To offer a more  competitive approach to attract students and address the two greatest  concerns prospective students and their parents face &ndash; the rising cost  of higher education and the increasing indebtedness with which college  students are graduating &ndash;Saint Louis Christian College began to offer  full tuition scholarships to all full time residential students.&nbsp; Full  time residential students are charged room and board fees and an  additional student services fee each semester.&nbsp; To address the needs of  our non-traditional AIM and commuter students&rsquo;, tuition was cut in half  for full time students.&nbsp; Any students not living on campus and taking  less than 12 hours are charged regular tuition rates.</p>
		<p>In  June of 2006, Dr. Guthrie Veech accepted the call from the Board of  Trustees to become the fifth president in the college&rsquo;s 50-year  history.&nbsp; He brings a balance of academic expertise from the college  classroom and ministry experience from the local congregation.</p>
		<p>While  personnel change periodically, the college&rsquo;s focus remains unchanged.&nbsp;  Saint Louis Christian College continues to exist to commit the gospel  of Christ to faithful people who will be able to teach others around  the globe.&nbsp; The mission remains ministry.&nbsp; Alumni of Saint Louis  Christian College serve throughout the United States and around the  world sharing the good news of Christ to faithful people so that they  in turn will be able to teach others the message of life.</p>
		<p>Saint  Louis Christian College is a private, church-related, coeducational  institution in the tradition of the Bible college movement.&nbsp; Seeking to  be faithful to the ideals of the Stone-Campbell Restoration Movement  heritage, Saint Louis Christian College is committed in all policies  and practices to the pursuit and exposition of truth as revealed in the  Bible, the Word of God, and in creation, the world of God.</p>
		<p>Saint  Louis Christian College recognizes that skilled leaders must have a  working foundation of general knowledge.&nbsp; We therefore provide for a  well-rounded education, enrolling students in courses focusing on  communication, the sciences, the humanities, and the individual&rsquo;s role  in society.</p>
		<p>Further, the college is committed to  providing a positive and supportive Christian environment in which  students can examine their giftedness, experiment in various ministry  possibilities, and engage their full potential in committing themselves  to a lifetime of servant-leadership to God, the church, and the world.&nbsp;  To achieve this end, instructional and institutional assessment efforts  regularly analyze the level of student learning and introduce changes  to improve student academic achievement.</p>
		<p>SLCC recruits  and retains highly qualified faculty members from within the membership  of the Christian Churches/Churches of Christ who are committed to the  pursuit of scholarship and instructional procedures that facilitate  student learning, excellence in the Word, servant leadership, and  ministry.</p>
		<p>Saint Louis Christian College offers both  traditional student and non-traditional adult undergraduate curricular  programs in both two-year (Associate of Arts in General Studies;  Associate of Applied Science in Intercultural &amp; Urban Missions,  Biblical Studies, or Preaching Ministry) and four-year (Bachelor of  Arts; Bachelor of Science) degrees.</p>
		<p>Saint Louis Christian  College adheres to the standards established by the ABHE and other  accrediting associations with which we affiliate.&nbsp; Saint Louis  Christian College is accredited by the Commission on Accreditation of  The Association for Biblical Higher Education (formerly The Accrediting  Association of Bible Colleges). </p>
		<p>Saint Louis Christian  College was granted candidacy status in 1974, accreditation status in  1977, and was reaffirmed in 1998. Member colleges of ABHE are also  recognized by CHEA (Council for Higher Education Accreditation), the  national accrediting organization that serves as an umbrella over all  the recognized accrediting bodies, including the regional associations. </p>
		<p>ABHE is also listed by the United States Department of  Education as a recognized agency for biblical higher education. The  Association's member institutions are recognized by the Department of  Justice, the Veteran's Administration, and other relevant federal  agencies in the United States. </p>
		<p>Finally, ABHE is  recognized by the International Council for Evangelical Theological  Education, the theological education affiliate of the World Evangelical  Alliance. </p>
		<p>The churches have indicated their approval of  Saint Louis Christian College by their support in financial gifts, by  sending men and women to be trained for Christian service, and by  employing Saint Louis Christian College alumni. The Missouri State  Department of Education has approved the college for the education of  veterans. Saint Louis Christian College, listed in the Directory of  Higher Education, is eligible for participation in many federal grant  and loan programs, such as the Federal Pell Grant Program, the Federal  Work-Study Program, the Federal Supplemental Educational Opportunity  Grant Program, the Federal Stafford Student Loan Program, and the  Federal PLUS Loan Program. </p>
		<p>As a Bible college, all  bachelor-degree students earn a major in Bible and a second major in  one of several areas: Educational Ministries (Children&rsquo;s Ministry;  Student Ministries; and Discipleship &amp; Involvement minors),  Intercultural &amp; Urban Missions, Preaching Ministry, Worship &amp;  Music Ministry, and Christian Ministry (non-traditional Adults In  Ministry program).&nbsp; The Christian worldview permeates all programs,  disciplines, and courses.&nbsp; As a result, the Bible is central,  integrating biblical principles and leadership training into the  historic tradition of the liberal arts and sciences.</p>
		<p>Saint  Louis Christian College desires to educate learners and to equip them  for service to God.&nbsp; Saint Louis Christian College seeks learners who  are:</p>
		<ul>
          <li>committed  to Christ, highly motivated to serve God and the church,</li>
		  <li>capable  of successfully completing the academic rigors of college education,</li>
		  <li>willing  to assimilate both knowledge and skills through service and mentored off-campus learning experiences,</li>
		  <li>drawn  from a diversity of geographic, ethnic, and socioeconomic backgrounds.</li>
		  </ul>
