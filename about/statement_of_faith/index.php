		<p> Without any effort to be exhaustive in setting forth the things we  believe, but rather to provide an accurate summary of our faith system,  the trustees, administration, and faculty affirm belief in the  following areas:</p>
		<p> <strong>God</strong> - We affirm our belief in the one eternal God, Creator, Lord, and  Savior of the world, Father, Son, and Holy Spirit, who governs all  things according to the purpose of His will.</p>
		<p> <strong>The Bible - </strong>We  affirm the divine inspiration, truthfulness, and authority of both Old  and New Testament Scriptures in their entirety as the only written word  of God, without error in all that it affirms, and the only infallible  rule of faith and practice.</p>
		<p> <strong>Man - </strong>We  affirm that all men and women are unique individuals, created in God's  image, capable of intimate fellowship with Him and with the potential  for personal fulfillment and positive social good. We further affirm  that all men and women, by virtue of Adam's sin and their own personal  rebellion, are sinners demonstrating their sinfulness in numerous  personal and social dimensions.</p>
		<p> <strong>Salvation through Christ - </strong>We  affirm that there is only one Savior and only one gospel. Jesus Christ,  being Himself the only God-man, who gave Himself as the only ransom for  sinners by His death on the cross, is the only mediator between God and  man. There is no other name by which we must be saved.</p>
		<p> <strong>Salvation by Faith - </strong>We affirm that individuals receive the benefits of Christ's salvation by a response of<br />
		  wholehearted faith in Him. This response of faith necessarily involves  repentance from sin, public confession of faith in Christ, baptism by  immersion, and lifelong obedience to Christ.</p>
		<p> <strong>The Church </strong> - We affirm that the church is the bride of Christ, the body of Christ,  the community of believers, and primary manifestation of the kingdom of  God on earth. The well-being of the believer, the progress of the  gospel, and the health of society at large are promoted by the vital  functioning of Christian congregations in every level of society.</p>
		<p> <strong>Evangelism -</strong> We affirm that Christ sends His redeemed people into the world as the  Father sent Him, and that this calls for a similar deep and costly  penetration of the world. World evangelization requires the whole  church to take the whole gospel to the whole world.</p>
		<p> <strong>Christian Unity</strong> - We affirm that division is a hindrance to the work of evangelism and  we recognize that Christ prayed that His followers might all be one. As  a result, we firmly advocate the restoration of the church revealed in  the Scriptures as the only sure way to achieve Christian unity.</p>
		<p> <strong>Social Responsibility</strong> - We affirm that God is both Creator and the Judge of all men. We  therefore should share His concern for justice and reconciliation  throughout human society and for the liberation of men from every kind  of oppression.</p>
		<p> <strong>The Power of the Holy Spirit</strong> - We believe in the power of the Holy Spirit. The Father sent His  Spirit to bear witness to His Son; without His witness ours is futile.  Conviction of sin, faith in Christ, new birth, and Christian growth are  all His work. We believe that the fruit of the Spirit (Gal. 5:22-23) is  the primary evidence of the Spirit's work in the believer's life.</p>
		<p> <strong>The Return of Christ </strong> - We believe that Jesus Christ will return personally and visibly, in  power and glory to consummate His salvation and His judgment. Our  Christian confidence is that God will perfect His kingdom, and we look  forward with eager anticipation to that day and to the new heaven and  earth in which righteousness will dwell and God will reign forever.</p>
