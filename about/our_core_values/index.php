		<p>As an  institution of Christian higher education, Saint Louis Christian   College embraces and  promotes:</p>
		<p><strong>Bible  knowledge - </strong>Saint  Louis Christian College is a Bible college where every baccalaureate  graduate pursues a major in Biblical and theological studies. The  Bible&rsquo;s message informs our understanding of the purpose and  predicament of human life, the history of the world, the nature and  mission of the Church, and the nature and goal of ministry.; <em>&ldquo;All  Scripture is God-breathed and is useful for teaching, rebuking,  correcting and training in righteousness, so that the man of God may be  thoroughly equipped for every good work.&rdquo;&nbsp; </em>(II Tim 3:16-17)</p>
		<p><strong>Christian  character -</strong> Saint Louis Christian College believes that servants of Christ must  live by the example and teaching of Christ.&nbsp; &nbsp; We believe that the  classical virtues of wisdom, courage, self-control, justice, faith,  hope, and love are exemplified by the life of Christ. He, in perfect  humility, taught us first to love God and then to love our neighbors as  ourselves.&nbsp; The college&rsquo;s Code of Student Conduct reflects and  encourages these values as do our hiring practices and academic  programs.&nbsp; <em>&ldquo;I gave you an example, that you also should do as I did to you.&rdquo;&nbsp;</em> (John 13:15)</p>
		<p><strong>Ministry  skills - </strong>Because  our graduates are called to be servant-leaders in their chosen fields  of ministry, they possess the skills to relate to people of many  backgrounds, the heart to serve them in their brokenness, and the  judgment to effectively apply the Scriptures to life&rsquo;s diverse  situations.&nbsp; Students are challenged and helped to identify and develop  their spiritual gifts and are placed in settings to practice those  gifts skillfully.&nbsp; <em>&ldquo;Each one should use whatever gift he has  received to serve others, faithfully administering God&rsquo;s grace in its  various forms.&rdquo;&nbsp; </em>(I Peter 4:10-11)</p>
		<p><strong>Christian  worldview -</strong> A broad base of academic knowledge is fundamental to becoming an  educated person who effectively reasons and interacts in society.&nbsp; At  the center of a Christian view of life and the world is the proposition  that humans and the universe in which they live were created by the God  who has revealed Himself in the Scriptures.&nbsp; God is the source of all  truth, not just religious truth; and the Bible provides the basis for  the ordering of all knowledge.&nbsp; The curriculum at Saint Louis Christian  College integrates thought and life across a broad academic range and  fosters in students a lifelong appreciation for the independent value  of knowing. &nbsp;&ldquo;The fear of the Lord is the beginning of knowledge.&rdquo;  (Proverbs 1:7) <em>&ldquo;And Jesus grew  in wisdom and stature, and in favor with God and men.&rdquo; </em>(Luke 2:52)</p>
		<p><strong> Christian community -</strong> Saint Louis Christian College is a diverse community of believers  united by Christ in mission and purpose.&nbsp; The intimacy of our campus  provides a unique atmosphere for developing lifelong relationships.&nbsp;  However, our community extends beyond the boundaries of our campus and  into the city in which we live, the church that we serve, and the world  we want to reach. <em>&ldquo;They devoted themselves to the apostles'  teaching and to the fellowship, to the breaking of bread and to  prayer.&nbsp; Everyone was filled with awe, and many wonders and miraculous  signs were done by the apostles. All the believers were together and  had everything in common. Selling their possessions and goods, they  gave to anyone as he had need.&nbsp; Every day they continued to meet  together in the temple courts. They broke bread in their homes and ate  together with glad and sincere hearts, praising God and enjoying the  favor of all the people. And the Lord added to their number daily those  who were being saved.&rdquo; </em>(Acts 2:42 &ndash; 47)&nbsp; (See also  Romans 12:4-8 and Ephesians 4: 11-13)</p>
